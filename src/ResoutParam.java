public class ResoutParam {
    private String mywords;
    private String backwords;

    public ResoutParam(String mywords, String backwords) {
        this.mywords = mywords;
        this.backwords = backwords;
    }
    public String getMywords() {
        return mywords;
    }

    public void setMywords(String mywords) {
        this.mywords = mywords;
    }

    public String getBackwords() {
        return backwords;
    }

    public void setBackwords(String backwords) {
        this.backwords = backwords;
    }
}
